package com.naver.myhome.meetup.domain;

public class Groups {
	
	private int num;
	private int joined_member;
	private int like_count;
	private int view_count;
	private int groups_member_num;
	
	private String name;
	private String subject;
	private String content;
	private String img_file;
	private String img_file_ori;
	private String regi_date;
	private String location;
	private String price;
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getJoined_member() {
		return joined_member;
	}
	public void setJoined_member(int joined_member) {
		this.joined_member = joined_member;
	}
	public int getLike_count() {
		return like_count;
	}
	public void setLike_count(int like_count) {
		this.like_count = like_count;
	}
	public int getView_count() {
		return view_count;
	}
	public void setView_count(int view_count) {
		this.view_count = view_count;
	}
	public int getGroups_member_num() {
		return groups_member_num;
	}
	public void setGroups_member_num(int groups_member_num) {
		this.groups_member_num = groups_member_num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImg_file() {
		return img_file;
	}
	public void setImg_file(String img_file) {
		this.img_file = img_file;
	}
	public String getImg_file_ori() {
		return img_file_ori;
	}
	public void setImg_file_ori(String img_file_ori) {
		this.img_file_ori = img_file_ori;
	}
	public String getRegi_date() {
		return regi_date;
	}
	public void setRegi_date(String regi_date) {
		this.regi_date = regi_date;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
 
}
